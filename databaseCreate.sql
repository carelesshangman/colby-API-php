  `vendor` varchar(128) DEFAULT NULL,
  `productType` varchar(64) DEFAULT NULL,
  `productID` int(11) DEFAULT NULL,
  `releaseDate` date DEFAULT NULL,
  `announced` int(11) DEFAULT NULL,
  `datumIzida` varchar(32) DEFAULT NULL,
  `packShot` varchar(512) DEFAULT NULL,
  `description` varchar(2048) DEFAULT NULL,
  `keyFeatures` varchar(2048) DEFAULT NULL,
  `legals` varchar(128) DEFAULT NULL,
  `spokenLanguages` varchar(128) DEFAULT NULL,
  `subtitleLanguages` varchar(256) DEFAULT NULL,
  `menuLanguages` varchar(256) DEFAULT NULL,
  `minRequirements` varchar(512) DEFAULT NULL,
  `recRequirements` varchar(512) DEFAULT NULL,
  `pegiValue` varchar(32) DEFAULT NULL,
  `pegiNumber` varchar(32) DEFAULT NULL,
  `videoURL` varchar(128) DEFAULT NULL,
  `genre` varchar(128) DEFAULT NULL,
